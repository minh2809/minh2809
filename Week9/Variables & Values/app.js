// I. Variables
// how to assign variable
// use let or var to declare and assign a variable
// if you use const, the variable cant be changed

let age = 30;
age = 31;

var newAge = 32;
newAge = 33;

const myAge = 34;
// myAge = 35;

console.log(age); // see on console 31
console.log(newAge); // see 32 on console
console.log(myAge);

